fun main() {
    // loop 'for' que irá percorrer do número 10 até o número 1, em ordem decrescente.
    for (i in 10 downTo 1) {
        // Printa o valor atual da variável 'i', seguido por um ponto e vírgula.
        print("$i; ")
    }
    // O loop termina após 'i' atingir o valor 1.
}